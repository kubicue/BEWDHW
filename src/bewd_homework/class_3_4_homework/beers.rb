puts "99 bottles of beers in wall."
count_bottles = 99

while (count_bottles > 1)  do
  puts "#{count_bottles} of beer on the wall, #{count_bottles} bottles of beer."

  count_bottles = count_bottles -1
  if count_bottles == 1
    puts "Take one down and pass it around, #{count_bottles} bottle of beer in the wall."
  else
    puts "Take one down and pass it around, #{count_bottles} bottle of beer in the wall."
  end

end

puts "1 bottle of beer on the wall, 1 bottle of beer."
